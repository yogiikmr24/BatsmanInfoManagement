    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
    String name=txtName.getText();
    String password=new String(txtPassword.getPassword());
    String confirmPassword=new String(txtCnfrmPassword.getPassword());
    String email=txtEmail.getText();
    if(!name.isEmpty() && !password.isEmpty() && !confirmPassword.isEmpty() && !email.isEmpty()){
       if(password.equals(confirmPassword)){
         lblMessage.setText("account crated");
       }
       else{
           lblMessage.setText("Incorrect password");
       }
    }
    else {
        lblMessage.setText("fill all details");
    }
    }//GEN-LAST:event_btnSignUpActionPerformed

   
   
    
